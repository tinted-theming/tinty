_tinty() {
    local i cur prev opts cmd
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="tinty"
                ;;
            tinty,apply)
                cmd="tinty__apply"
                ;;
            tinty,current)
                cmd="tinty__current"
                ;;
            tinty,help)
                cmd="tinty__help"
                ;;
            tinty,info)
                cmd="tinty__info"
                ;;
            tinty,init)
                cmd="tinty__init"
                ;;
